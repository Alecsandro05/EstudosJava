package estudo.cursoJava.exercicios.RevisaoConteudo.Modulo1;

/*
* Crie um programa que receba dois números e imprima: A soma, A subtração, A multiplicação, A divisão
*/

public class Desafio2 {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;
        System.out.println("A soma de " + num1 + " + " + num2 + " é igual a " + (num1 + num2));
        System.out.println("A subtração de " + num1 + " - " + num2 + " é igual a " + (num1 - num2));
        System.out.println("A multiplicação de " + num1 + " * " + num2 + " é igual a " + (num1 * num2));
        System.out.println("A divisão de " + num1 + " / " + num2 + " é igual a " + (num1 / num2));

    }
}
