package estudo.cursoJava.exercicios.aula10;

public class Tabuada {
    public static void main(String[] args) {
        //Escreva um programa que exiba a tabuada de um número de 1 a 10.çç
        int num = 5;
        for (int i = 0; i <= 10; i++){
            System.out.println(i + " * " +  num + " = " + (i*num) );
        }
    }
}
